function cfg = compactConfig(options)
%COMPACTCONFIG Fast deterministic attenuation benchmark configuration.

arguments
    options.ShearAlphaRefDbCm (1,1) double = 1.0
    options.ShearReferenceFrequencyHz (1,1) double = 500
    options.ShearPowerY (1,1) double = 1.2
    options.CompressionAlphaRefDbCm (1,1) double = 0.1
    options.CompressionReferenceFrequencyHz (1,1) double = 500
    options.CompressionPowerY (1,1) double = 1.2
    options.Seed (1,1) double = 1001
end

cfg = kwsim_benchmarks.attenuation_power_law_2d.config( ...
    ShearAlphaRefDbCm=options.ShearAlphaRefDbCm, ...
    ShearReferenceFrequencyHz= ...
        options.ShearReferenceFrequencyHz, ...
    ShearPowerY=options.ShearPowerY, ...
    CompressionAlphaRefDbCm= ...
        options.CompressionAlphaRefDbCm, ...
    CompressionReferenceFrequencyHz= ...
        options.CompressionReferenceFrequencyHz, ...
    CompressionPowerY=options.CompressionPowerY, ...
    Seed=options.Seed);

cfg.solver.pml_size_points = 8;
cfg.sensor.source_buffer_m = 2e-3;
cfg.sensor.boundary_margin_m = 2e-3;
cfg.time.settling_cycles = 1;
cfg.output.directory = "";

end
