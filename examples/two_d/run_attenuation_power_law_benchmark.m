function sweep = run_attenuation_power_law_benchmark( ...
        output_directory, options)
%RUN_ATTENUATION_POWER_LAW_BENCHMARK Run and save the reference benchmark.
%
% Example:
%
% sweep = run_attenuation_power_law_benchmark( ...
%     "outputs/my_attenuation", ...
%     ShearAlphaRefDbCm=1.5, ...
%     ShearPowerY=1.1, ...
%     Seed=1001);

arguments
    output_directory {mustBeTextScalar} = fullfile( ...
        kwsim.io.projectRoot(), ...
        "outputs", ...
        "attenuation_power_law")

    options.ShearAlphaRefDbCm (1,1) double = 1.0
    options.ShearReferenceFrequencyHz (1,1) double = 500
    options.ShearPowerY (1,1) double = 1.2
    options.Seed (1,1) double = 1001
end

cfg = kwsim_benchmarks.attenuation_power_law_2d.config( ...
    ShearAlphaRefDbCm=options.ShearAlphaRefDbCm, ...
    ShearReferenceFrequencyHz= ...
        options.ShearReferenceFrequencyHz, ...
    ShearPowerY=options.ShearPowerY, ...
    Seed=options.Seed);

frequencies_hz = [300, 400, 500];

sweep = kwsim_benchmarks.attenuation_power_law_2d.run( ...
    cfg, frequencies_hz, output_directory);

fprintf('%s\n', sweep.summary);

end
