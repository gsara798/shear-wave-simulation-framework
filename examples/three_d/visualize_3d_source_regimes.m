%VISUALIZE_3D_SOURCE_REGIMES Visualize all canonical 3D source regimes.
%
% This example resolves source geometry using DryRun=true. It does not
% execute the k-Wave solver or create simulation-result folders.

clear;
clc;

script_directory = ...
    fileparts(mfilename("fullpath"));

repository_root = ...
    fileparts(fileparts(script_directory));

addpath(fullfile(repository_root, "src"));

configuration_directory = ...
    fullfile( ...
        repository_root, ...
        "configs", ...
        "three_d");

configuration_files = [
    "homogeneous_directional_req_validation.json"
    "homogeneous_partial_diffuse8_req_validation.json"
    "homogeneous_partial_3d_n8_p2_req_validation.json"
    "homogeneous_generated_angular_n8_p2_req_validation.json"
    "homogeneous_generated_angular_n8_p4_req_validation.json"
    "homogeneous_generated_angular_n32_p8_req_validation.json"
    "homogeneous_generated_angular_n128_p8_req_validation.json"
];

regime_names = [
    "directional_N1"
    "single_face_N8"
    "legacy_multiface_N8_P2"
    "generated_N8_P2"
    "generated_N8_P4"
    "generated_N32_P8"
    "packed_N128_P8"
];

output_directory = ...
    fullfile( ...
        repository_root, ...
        "outputs", ...
        "examples", ...
        "three_d_source_regimes");

if ~isfolder(output_directory)
    mkdir(output_directory);
end

number_of_regimes = ...
    numel(configuration_files);

N = ...
    zeros(number_of_regimes, 1);

P = ...
    zeros(number_of_regimes, 1);

faces = ...
    zeros(number_of_regimes, 1);

Deff = ...
    zeros(number_of_regimes, 1);

bias = ...
    zeros(number_of_regimes, 1);

axis_p10_deg = ...
    nan(number_of_regimes, 1);

axis_median_deg = ...
    nan(number_of_regimes, 1);

style = ...
    kwsim.viz.figureTemplate();

fprintf("\nCreating 3D source-regime gallery...\n");

for regime_index = 1:number_of_regimes
    configuration_file = ...
        fullfile( ...
            configuration_directory, ...
            configuration_files(regime_index));

    fprintf( ...
        "\n[%d/%d] %s\n", ...
        regime_index, ...
        number_of_regimes, ...
        regime_names(regime_index));

    [requested_cfg, ~] = ...
        kwsim.io.loadConfigJson( ...
            configuration_file);

    % This example visualizes resolved source geometry only. Numerical
    % points-per-wavelength acceptance is evaluated by the physical solver
    % configurations and should not block geometry visualization.
    requested_cfg.grid.minimum_shear_ppw = ...
        1;

    [cfg, ~] = ...
        kwsim.three_d.validateConfig( ...
            requested_cfg);

    geometry = ...
        cfg.source.geometry_metrics;

    N(regime_index) = ...
        geometry.source_count;

    P(regime_index) = ...
        geometry.in_plane_contact_count;

    faces(regime_index) = ...
        geometry.unique_face_count;

    Deff(regime_index) = ...
        geometry.effective_angular_dimension;

    bias(regime_index) = ...
        geometry.directional_bias;

    if isfield(geometry, "pairwise_axis_angle_p10_deg")
        axis_p10_deg(regime_index) = ...
            geometry.pairwise_axis_angle_p10_deg;
    end

    if isfield(geometry, "pairwise_axis_angle_median_deg")
        axis_median_deg(regime_index) = ...
            geometry.pairwise_axis_angle_median_deg;
    end

    handles = ...
        kwsim.viz.plotSourceGeometry3D( ...
            cfg, ...
            Title=replace( ...
                regime_names(regime_index), ...
                "_", ...
                " "), ...
            FigureVisible="off", ...
            ShowContactNodes=false, ...
            ShowDirectionArrows=true);

    output_file = ...
        fullfile( ...
            output_directory, ...
            lower(regime_names(regime_index)) + ...
                "_source_geometry.png");

    exportgraphics( ...
        handles.figure, ...
        output_file, ...
        "Resolution", ...
        style.export_resolution_dpi, ...
        "BackgroundColor", ...
        style.background_color);

    close(handles.figure);

    fprintf( ...
        "Saved: %s\n", ...
        output_file);
end

summary = ...
    table( ...
        regime_names, ...
        N, ...
        P, ...
        faces, ...
        Deff, ...
        bias, ...
        axis_p10_deg, ...
        axis_median_deg);

summary_file = ...
    fullfile( ...
        output_directory, ...
        "source_regime_summary.csv");

writetable( ...
    summary, ...
    summary_file);

fprintf("\n============================================\n");
fprintf("3D source-regime gallery completed\n");
fprintf("============================================\n");

disp(summary);

fprintf( ...
    "Figures and summary:\n%s\n", ...
    output_directory);
