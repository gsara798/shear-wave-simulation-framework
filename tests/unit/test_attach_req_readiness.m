function tests = test_attach_req_readiness
%TEST_ATTACH_REQ_READINESS Tests for combined physical/REQ validation.

tests = functiontests(localfunctions);

end


function setupOnce(~)

repository_root = fileparts(fileparts(fileparts( ...
    mfilename("fullpath"))));

addpath(fullfile(repository_root, "src"));

end


function testValidPhysicalAndReqRemainValid(testCase)

physical = ...
    makePhysicalReport(true);

req = ...
    makeReqReport(true);

report = ...
    kwsim.validation. ...
    attachReqReadiness(physical, req);

verifyTrue(testCase, report.valid);
verifyTrue(testCase, report.physical_valid);
verifyTrue(testCase, report.req_validation_evaluated);
verifyTrue(testCase, report.req_validation.valid);

check = ...
    report.checks(end);

verifyEqual(testCase, ...
    check.name, ...
    "req_validation_sample_ready");

verifyTrue(testCase, ...
    check.pass);

verifyTrue(testCase, ...
    contains(report.summary, "REQ-ready=1"));

end


function testInvalidReqInvalidatesCombinedReport(testCase)

physical = ...
    makePhysicalReport(true);

req = ...
    makeReqReport(false);

report = ...
    kwsim.validation. ...
    attachReqReadiness(physical, req);

verifyFalse(testCase, report.valid);
verifyTrue(testCase, report.physical_valid);
verifyFalse(testCase, report.req_validation.valid);
verifyFalse(testCase, report.checks(end).pass);

end


function testInvalidPhysicalRemainsInvalid(testCase)

physical = ...
    makePhysicalReport(false);

req = ...
    makeReqReport(true);

report = ...
    kwsim.validation. ...
    attachReqReadiness(physical, req);

verifyFalse(testCase, report.valid);
verifyFalse(testCase, report.physical_valid);
verifyTrue(testCase, report.req_validation.valid);

end


function report = makePhysicalReport(valid)

check = struct( ...
    "name", ...
        "finite_harmonic_fields", ...
    "pass", ...
        logical(valid), ...
    "value", ...
        double(valid), ...
    "threshold", ...
        1, ...
    "message", ...
        "Synthetic physical check.");

report = struct();

report.valid = ...
    logical(valid);

report.checks = ...
    check;

report.summary = ...
    "physical valid=" + string(double(valid));

end


function report = makeReqReport(valid)

check = struct( ...
    "name", ...
        "finite_wavefield", ...
    "pass", ...
        logical(valid), ...
    "value", ...
        double(valid), ...
    "threshold", ...
        1, ...
    "meaning", ...
        "Synthetic REQ check.");

report = struct();

report.valid = ...
    logical(valid);

report.checks = ...
    check;

report.summary = ...
    "REQ-ready=" + string(double(valid));

end
