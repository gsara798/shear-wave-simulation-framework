function cfg = makeBaseConfig()

cfg = ...
    kwsim.three_d.defaultConfig();

cfg.geometry.minimum_boundary_clearance_m = ...
    1e-3;

end


function object = makeObject( ...
        type, ...
        name, ...
        center_m_xyz, ...
        radius_m, ...
        material_id, ...
        cs_m_s)

object = struct();
object.type = string(type);
object.name = string(name);
object.center_m_xyz = center_m_xyz;
object.axis_xyz = [NaN, NaN, NaN];
object.radius_m = radius_m;
object.length_m = NaN;
object.material_id = material_id;
object.cs_m_s = cs_m_s;
object.cp_m_s = NaN;
object.rho_kg_m3 = NaN;

end


function material = makeMaterial( ...
        material_id, ...
        cs_m_s)

material = struct();
material.material_id = material_id;
material.cs_m_s = cs_m_s;
material.cp_m_s = NaN;
material.rho_kg_m3 = NaN;

end


function index_xyz = physicalPointToIndex( ...
        point_m_xyz, ...
        cfg)

spacing_m_xyz = [
    cfg.grid.dx_m, ...
    cfg.grid.dy_m, ...
    cfg.grid.dz_m
];

index_xyz = ...
    round(point_m_xyz ./ spacing_m_xyz) + ...
    1;

end


function repository_root = findRepositoryRoot()

repository_root = ...
    fileparts( ...
        fileparts( ...
        fileparts( ...
            mfilename("fullpath"))));

end
