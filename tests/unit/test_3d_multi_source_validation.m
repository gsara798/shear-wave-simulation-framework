function tests = test_3d_multi_source_validation
%TEST_3D_MULTI_SOURCE_VALIDATION Unit tests for multi-source validation.

tests = functiontests(localfunctions);

end


function setupOnce(~)

repository_root = fileparts(fileparts(fileparts( ...
    mfilename("fullpath"))));

addpath(fullfile(repository_root, "src"));

end


function testDirectionalFailuresRemainDescriptive(testCase)

[result, directional_report] = ...
    syntheticCase();

verifyFalse(testCase, ...
    directional_report.valid);

report = ...
    kwsim.validation. ...
    evaluateMultiSourceHarmonic3D( ...
        result, ...
        DirectionalReport=directional_report);

verifyTrue(testCase, ...
    report.valid, ...
    report.summary);

verifyFalse(testCase, ...
    report.directional_thresholds_applicable);

verifyEqual(testCase, ...
    report.scope, ...
    "homogeneous_multi_source_harmonic_3d");

verifyEqual(testCase, ...
    report.metrics.apparent_directional_speed.speed_m_s, ...
    2.75);

verifyGreaterThan(testCase, ...
    report.metrics.apparent_speed_relative_error, ...
    0.30);

check_names = ...
    string([report.checks.name]);

verifyFalse(testCase, ...
    any(check_names == ...
        "shear_speed_relative_error"));

verifyFalse(testCase, ...
    any(check_names == ...
        "directional_core_cross_polarization_ratio"));

verifyFalse(testCase, ...
    any(check_names == ...
        "y_even_symmetry_error"));

end


function testLongitudinalLeakageIsAcceptanceCheck(testCase)

[result, directional_report] = ...
    syntheticCase();

directional_report.metrics.field. ...
    longitudinal_leakage_ratio = 0.25;

report = ...
    kwsim.validation. ...
    evaluateMultiSourceHarmonic3D( ...
        result, ...
        DirectionalReport=directional_report);

verifyFalse(testCase, ...
    report.valid);

check = ...
    findCheck( ...
        report, ...
        "global_longitudinal_leakage_ratio");

verifyFalse(testCase, ...
    check.pass);

end


