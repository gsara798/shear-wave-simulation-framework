function tests = test_3d_multiface_bank_resolver
%TEST_3D_MULTIFACE_BANK_RESOLVER Unit tests for generic face-bank resolution.

tests = functiontests(localfunctions);

end

function testResolvesSixFaceBank(testCase)

cfg = makeBaseConfig();
specs = makeSixFaceSpecs();
cfg.source.vibrator_count = numel(specs);

bank = ...
    kwsim.three_d.resolveVibratorBankFromFaceSpecs( ...
        cfg, ...
        specs);

verifyEqual(testCase, bank.contact_count, 6);
verifyEqual(testCase, numel(bank.vibrators), 6);
verifyEqual(testCase, bank.faces, [specs.face].');
verifyEqual(testCase, bank.face_counts, ones(6, 1));
verifyEqual(testCase, nnz(bank.union_mask_xyz), ...
    bank.total_contact_node_count);

for vibrator_index = 1:numel(bank.vibrators)
    vibrator = bank.vibrators(vibrator_index);

    verifyTrue( ...
        testCase, ...
        all(bank.union_mask_xyz(vibrator.node_linear_indices)));

    verifyEqual( ...
        testCase, ...
        vibrator.contact_node_count, ...
        numel(vibrator.node_linear_indices));

    fixed_axis = ...
        fixedAxisForFace(vibrator.face);

    [node_x, node_y, node_z] = ...
        ind2sub( ...
            size(bank.union_mask_xyz), ...
            vibrator.node_linear_indices);

    node_subscripts_xyz = ...
        [node_x, node_y, node_z];

    verifyEqual( ...
        testCase, ...
        unique(node_subscripts_xyz(:, fixed_axis)), ...
        vibrator.center_index_xyz(fixed_axis));
end

end

function testDirectionsPointInwardAndPolarizationsAreTransverse(testCase)

cfg = makeBaseConfig();
specs = makeSixFaceSpecs();
cfg.source.vibrator_count = numel(specs);

bank = ...
    kwsim.three_d.resolveVibratorBankFromFaceSpecs( ...
        cfg, ...
        specs);

domain_center_m_xyz = ...
    0.5 * ( ...
        [cfg.grid.Nx, cfg.grid.Ny, cfg.grid.Nz] - 1) .* ...
        [cfg.grid.dx_m, cfg.grid.dy_m, cfg.grid.dz_m];

for vibrator_index = 1:numel(bank.vibrators)
    vibrator = bank.vibrators(vibrator_index);

    expected_direction = ...
        domain_center_m_xyz - ...
        vibrator.center_m_xyz;

    expected_direction = ...
        expected_direction / norm(expected_direction);

    verifyEqual( ...
        testCase, ...
        vibrator.nominal_propagation_xyz, ...
        expected_direction, ...
        "AbsTol", ...
        1e-12);

    verifyEqual( ...
        testCase, ...
        norm(vibrator.nominal_propagation_xyz), ...
        1, ...
        "AbsTol", ...
        1e-12);

    verifyEqual( ...
        testCase, ...
        norm(vibrator.polarization_xyz), ...
        1, ...
        "AbsTol", ...
        1e-12);

    verifyEqual( ...
        testCase, ...
        dot( ...
            vibrator.nominal_propagation_xyz, ...
            vibrator.polarization_xyz), ...
        0, ...
        "AbsTol", ...
        1e-12);

    verifyGreaterThan( ...
        testCase, ...
        dot( ...
            vibrator.nominal_propagation_xyz, ...
            -vibrator.face_normal_xyz), ...
        0);
end

end

function testDriveNormalization(testCase)

cfg = makeBaseConfig();
specs = makeSixFaceSpecs();
cfg.source.vibrator_count = numel(specs);

bank = ...
    kwsim.three_d.resolveVibratorBankFromFaceSpecs( ...
        cfg, ...
        specs);

verifyEqual( ...
    testCase, ...
    bank.nominal_total_drive_rms_squared_m2_s2, ...
    bank.reference_single_contact_drive_rms_squared_m2_s2, ...
    "RelTol", ...
    1e-12);

node_counts = ...
    [bank.vibrators.contact_node_count].';

amplitudes = ...
    [bank.vibrators.velocity_amplitude_m_s].';

per_contact_drive = ...
    node_counts .* amplitudes.^2 / 2;

verifyEqual( ...
    testCase, ...
    per_contact_drive, ...
    repmat( ...
        bank.reference_single_contact_drive_rms_squared_m2_s2 / ...
        bank.contact_count, ...
        bank.contact_count, ...
        1), ...
    "RelTol", ...
    1e-12);

end

function testDeterministicWithoutChangingGlobalRandomState(testCase)

