specs(1) = struct( ...
    "name", "x_min_contact", ...
    "face", "x_min", ...
    "center_fraction_tangent", [0.20, 0.25]);

specs(2) = struct( ...
    "name", "x_max_contact", ...
    "face", "x_max", ...
    "center_fraction_tangent", [0.75, 0.70]);

specs(3) = struct( ...
    "name", "y_min_contact", ...
    "face", "y_min", ...
    "center_fraction_tangent", [0.30, 0.75]);

specs(4) = struct( ...
    "name", "y_max_contact", ...
    "face", "y_max", ...
    "center_fraction_tangent", [0.70, 0.30]);

specs(5) = struct( ...
    "name", "z_min_contact", ...
    "face", "z_min", ...
    "center_fraction_tangent", [0.25, 0.65]);

specs(6) = struct( ...
    "name", "z_max_contact", ...
    "face", "z_max", ...
    "center_fraction_tangent", [0.70, 0.20]);

end

function fixed_axis = fixedAxisForFace(face)

switch string(face)
    case {"x_min", "x_max"}
        fixed_axis = 1;

    case {"y_min", "y_max"}
        fixed_axis = 2;

    case {"z_min", "z_max"}
        fixed_axis = 3;
end

end
