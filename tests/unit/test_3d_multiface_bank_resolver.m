end

function testFractionEndpointsMapToValidExtremes(testCase)

cfg = makeBaseConfig();
cfg.source.vibrator_count = 1;

low_spec = struct( ...
    "name", "low", ...
    "face", "x_min", ...
    "center_fraction_tangent", [0, 0]);

high_spec = struct( ...
    "name", "high", ...
    "face", "x_min", ...
    "center_fraction_tangent", [1, 1]);

low_bank = ...
    kwsim.three_d.resolveVibratorBankFromFaceSpecs( ...
        cfg, ...
        low_spec);

high_bank = ...
    kwsim.three_d.resolveVibratorBankFromFaceSpecs( ...
        cfg, ...
        high_spec);

radius_points_y = ...
    ceil(cfg.source.contact_radius_m / cfg.grid.dy_m);

radius_points_z = ...
    ceil(cfg.source.contact_radius_m / cfg.grid.dz_m);

verifyEqual( ...
    testCase, ...
    low_bank.vibrators.center_index_xyz(2:3), ...
    [1 + radius_points_y, 1 + radius_points_z]);

verifyEqual( ...
    testCase, ...
    high_bank.vibrators.center_index_xyz(2:3), ...
    [cfg.grid.Ny - radius_points_y, ...
     cfg.grid.Nz - radius_points_z]);

end

