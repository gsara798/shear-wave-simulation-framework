function cfg = makeBaseConfig()

cfg = ...
    kwsim.three_d.defaultConfig();

cfg.grid.Nx = 52;
cfg.grid.Ny = 46;
cfg.grid.Nz = 50;

cfg.grid.dx_m = 0.4e-3;
cfg.grid.dy_m = 0.5e-3;
cfg.grid.dz_m = 0.6e-3;

cfg.source.boundary_margin_m = 4e-3;
cfg.source.contact_radius_m = 1.2e-3;
cfg.source.contact_node_spacing_points = 2;

cfg.source.phase_policy = "random_uniform";
cfg.source.amplitude_policy = "equal_total_rms";
cfg.source.polarization_policy = ...
    "project_axial_transverse";

end

