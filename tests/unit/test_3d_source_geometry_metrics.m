end


function setupOnce(~)

repository_root = ...
    findRepositoryRoot();

addpath(fullfile(repository_root, "src"));

end


function testSingleDirectionHasDimensionOne(testCase)

cfg = ...
    makeSyntheticConfig( ...
        [1, 0, 0], ...
        "x_min");

metrics = ...
    kwsim.analysis.summarizeSourceGeometry3D(cfg);

verifyEqual( ...
    testCase, ...
    metrics.effective_angular_dimension, ...
    1, ...
    "AbsTol", ...
    1e-12);

verifyEqual( ...
    testCase, ...
    metrics.directional_bias, ...
    1, ...
    "AbsTol", ...
    1e-12);

verifyEqual( ...
    testCase, ...
    metrics.angular_eigenvalues_descending, ...
    [1; 0; 0], ...
    "AbsTol", ...
    1e-12);

end


