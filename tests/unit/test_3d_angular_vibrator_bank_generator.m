function testGenerationIsDeterministic(testCase)

cfg = ...
    makeConfig(8);

first = ...
    kwsim.three_d.generateAngularVibratorBank( ...
        cfg, ...
        8, ...
        2, ...
        Seed=8102, ...
        CandidateCount=150, ...
        MinimumUniqueFaces=4);

second = ...
    kwsim.three_d.generateAngularVibratorBank( ...
        cfg, ...
        8, ...
        2, ...
        Seed=8102, ...
        CandidateCount=150, ...
        MinimumUniqueFaces=4);

verifyEqual( ...
    testCase, ...
    first.desired_directions_xyz, ...
    second.desired_directions_xyz);

verifyEqual( ...
    testCase, ...
    first.contact_specs, ...
    second.contact_specs);

verifyEqual( ...
    testCase, ...
    first.score, ...
    second.score, ...
    "AbsTol", ...
    1e-12);

verifyEqual( ...
    testCase, ...
    first.candidate_index, ...
    second.candidate_index);

end


function cfg = makeConfig(vibrator_count)

cfg = ...
    kwsim.three_d.defaultConfig();

cfg.grid.Nx = ...
    48;

cfg.grid.Ny = ...
    32;

cfg.grid.Nz = ...
    48;

cfg.source.layout = ...
    "vibrator_bank";

cfg.source.side = ...
    "multiface";

cfg.source.vibrator_count = ...
    vibrator_count;

cfg.source.phase_policy = ...
    "random_uniform";

cfg.source.amplitude_policy = ...
    "equal_total_rms";

cfg.source.polarization_policy = ...
    "project_axial_transverse";

cfg.source.contact_radius_m = ...
    1.0e-3;

cfg.source.boundary_margin_m = ...
    2.0e-3;

cfg.seed = ...
    12345;

end
