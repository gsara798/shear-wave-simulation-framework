function setupOnce(~)

repository_root = ...
    fileparts( ...
        fileparts( ...
        fileparts( ...
        mfilename("fullpath"))));

addpath(fullfile(repository_root, "src"));

end


function testGeneratesThreeDimensionalN8P2Bank(testCase)

cfg = ...
    makeConfig(8);

generated = ...
    kwsim.three_d.generateAngularVibratorBank( ...
        cfg, ...
        8, ...
        2, ...
        Seed=8202, ...
        CandidateCount=400, ...
        MinimumUniqueFaces=5, ...
        MinimumEffectiveDimension=2.20, ...
        MaximumDirectionalBias=0.35, ...
        MinimumThirdEigenvalue=0.12, ...
        MinimumAxisSeparationDeg=8, ...
        MaximumMappingErrorDeg=8);

metrics = ...
    generated.geometry_metrics;

verifyEqual( ...
    testCase, ...
    numel(generated.bank.vibrators), ...
    8);

verifyEqual( ...
    testCase, ...
    metrics.in_plane_contact_count, ...
    2);

verifyEqual( ...
    testCase, ...
    metrics.centered_in_plane_contact_count, ...
    2);

verifyEqual( ...
    testCase, ...
    metrics.out_of_plane_contact_count, ...
    6);

verifyGreaterThanOrEqual( ...
    testCase, ...
    metrics.unique_face_count, ...
    5);

verifyGreaterThanOrEqual( ...
    testCase, ...
    metrics.effective_angular_dimension, ...
    2.20);

verifyLessThanOrEqual( ...
    testCase, ...
    metrics.directional_bias, ...
    0.35);

verifyGreaterThanOrEqual( ...
    testCase, ...
    metrics.angular_eigenvalues_descending(3), ...
    0.12);

verifyGreaterThanOrEqual( ...
    testCase, ...
    metrics.pairwise_axis_angle_min_deg, ...
    8);

verifyLessThanOrEqual( ...
    testCase, ...
    generated.mapping.maximum_angular_error_deg, ...
    8);

end


