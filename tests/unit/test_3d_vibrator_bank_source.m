function testMaskAndSourceMatrixDimensions(testCase)

[cfg, kgrid] = resolvedSyntheticBank();

[source, metadata] = ...
    kwsim.three_d.buildVibratorBankSource( ...
        cfg, ...
        kgrid);

expected_source_points = ...
    sum(arrayfun( ...
        @(vibrator) numel(vibrator.node_linear_indices), ...
        cfg.source.vibrators));

expected_size = [
    expected_source_points, ...
    numel(kgrid.t_array)
];

verifySize(testCase, ...
    source.u_mask, ...
    [cfg.grid.Nx, cfg.grid.Ny, cfg.grid.Nz]);

verifyEqual(testCase, ...
    nnz(source.u_mask), ...
    expected_source_points);

verifySize(testCase, ...
    source.ux, ...
    expected_size);

verifySize(testCase, ...
    source.uy, ...
    expected_size);

verifySize(testCase, ...
    source.uz, ...
    expected_size);

verifyEqual(testCase, ...
    metadata.vibrator_count, ...
    2);

verifyEqual(testCase, ...
    metadata.contact_node_count, ...
    expected_source_points);

verifyEqual(testCase, ...
    metadata.active_linear_indices, ...
    find(source.u_mask));

end


function testWaveformsFollowLinearMaskOrdering(testCase)

[cfg, kgrid] = resolvedSyntheticBank();

[source, metadata] = ...
    kwsim.three_d.buildVibratorBankSource( ...
        cfg, ...
        kgrid);

t_s = ...
    double(kgrid.t_array(:).');

ramp_duration_s = ...
    cfg.source.ramp_cycles / ...
    cfg.source.f0_hz;

envelope = ...
    ones(size(t_s));

ramp_index = ...
    t_s < ramp_duration_s;

envelope(ramp_index) = ...
    0.5 * ...
    (1 - cos( ...
        pi*t_s(ramp_index) / ...
        ramp_duration_s));

for vibrator_index = 1:numel(cfg.source.vibrators)
    vibrator = ...
        cfg.source.vibrators(vibrator_index);

    rows = ...
        metadata.owner_vibrator_index_by_source_row == ...
        vibrator_index;

    waveform = ...
        vibrator.velocity_amplitude_m_s .* ...
        envelope .* ...
        sin( ...
            2*pi*cfg.source.f0_hz*t_s + ...
            vibrator.phase_rad);

    polarization = ...
        vibrator.polarization_xyz;

    expected_ux = repmat( ...
        single(polarization(1) .* waveform), ...
        nnz(rows), ...
        1);

    expected_uy = repmat( ...
        single(polarization(2) .* waveform), ...
        nnz(rows), ...
        1);

    expected_uz = repmat( ...
        single(polarization(3) .* waveform), ...
        nnz(rows), ...
        1);

    verifyEqual(testCase, ...
        source.ux(rows, :), ...
        expected_ux);

    verifyEqual(testCase, ...
        source.uy(rows, :), ...
        expected_uy);

    verifyEqual(testCase, ...
        source.uz(rows, :), ...
        expected_uz);
end

end


function testEqualTotalRmsNormalization(testCase)

[cfg, kgrid, base_amplitude] = ...
    resolvedSyntheticBank();

[~, metadata] = ...
    kwsim.three_d.buildVibratorBankSource( ...
        cfg, ...
        kgrid);

nodes_per_vibrator = ...
    numel(cfg.source.vibrators(1).node_linear_indices);

expected_total_rms_squared = ...
    nodes_per_vibrator * ...
    base_amplitude^2 / 2;

verifyEqual(testCase, ...
    metadata.nominal_total_drive_rms_squared_m2_s2, ...
    expected_total_rms_squared, ...
    "RelTol", ...
    1e-12);

end


