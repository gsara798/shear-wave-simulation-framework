end


function [cfg, kgrid, base_amplitude] = ...
    resolvedSyntheticBank()

cfg = kwsim.three_d.defaultConfig();

cfg.grid.Nx = 8;
cfg.grid.Ny = 9;
cfg.grid.Nz = 10;

cfg.source.layout = ...
    "vibrator_bank";

cfg.source.phase_policy = ...
    "explicit";

cfg.source.amplitude_policy = ...
    "equal_total_rms";

base_amplitude = ...
    cfg.source.velocity_amplitude_m_s;

per_vibrator_amplitude = ...
    base_amplitude / sqrt(2);

mask_1 = false( ...
    cfg.grid.Nx, ...
    cfg.grid.Ny, ...
    cfg.grid.Nz);

mask_1(2, 2, 3) = true;
mask_1(2, 4, 3) = true;

mask_2 = false( ...
    cfg.grid.Nx, ...
    cfg.grid.Ny, ...
    cfg.grid.Nz);

mask_2(2, 6, 7) = true;
mask_2(2, 8, 7) = true;

template = struct( ...
    "name", "", ...
    "node_linear_indices", [], ...
    "center_index_xyz", [NaN, NaN, NaN], ...
    "center_m_xyz", [NaN, NaN, NaN], ...
    "polarization_xyz", [NaN, NaN, NaN], ...
    "nominal_propagation_xyz", [NaN, NaN, NaN], ...
    "phase_rad", NaN, ...
    "velocity_amplitude_m_s", NaN);

vibrators = ...
    repmat(template, 2, 1);

vibrators(1).name = ...
    "vibrator_1";

vibrators(1).node_linear_indices = ...
    find(mask_1);

vibrators(1).center_index_xyz = ...
    [2, 3, 3];

vibrators(1).center_m_xyz = ...
    [0.5e-3, 1.0e-3, 1.0e-3];

vibrators(1).polarization_xyz = ...
    [0, 0, 1];

vibrators(1).nominal_propagation_xyz = ...
    [1, 0, 0];

vibrators(1).phase_rad = ...
    0;

vibrators(1).velocity_amplitude_m_s = ...
    per_vibrator_amplitude;

vibrators(2).name = ...
    "vibrator_2";

vibrators(2).node_linear_indices = ...
    find(mask_2);

vibrators(2).center_index_xyz = ...
    [2, 7, 7];

vibrators(2).center_m_xyz = ...
    [0.5e-3, 3.0e-3, 3.0e-3];

vibrators(2).polarization_xyz = ...
    [0, 1, 0];

vibrators(2).nominal_propagation_xyz = ...
    [1, 0, 0];

vibrators(2).phase_rad = ...
    pi/2;

vibrators(2).velocity_amplitude_m_s = ...
    per_vibrator_amplitude;

cfg.source.vibrators = ...
    vibrators;

kgrid = struct();

sampling_frequency_hz = ...
    50e3;

duration_s = ...
    12 / cfg.source.f0_hz;

kgrid.t_array = ...
    0:(1/sampling_frequency_hz):duration_s;

end
