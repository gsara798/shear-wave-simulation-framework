function tests = test_heterogeneous_multifield_configs
%TEST_HETEROGENEOUS_MULTIFIELD_CONFIGS
% Validate heterogeneous geometries combined with multiface source banks.

tests = functiontests(localfunctions);

end


function setupOnce(~)

repository_root = findRepositoryRoot();
addpath(fullfile(repository_root, "src"));

end


function testSphereWithN8P2(testCase)

cfg = loadResolvedConfig( ...
    "heterogeneous_sphere_partial_3d_n8_p2.json");

verifyEqual( ...
    testCase, ...
    string(cfg.source.layout), ...
    "vibrator_bank");

verifyEqual( ...
    testCase, ...
    double(cfg.source.vibrator_count), ...
    8);

verifyEqual( ...
    testCase, ...
    numel(cfg.source.vibrators), ...
    8);

verifyBankContract(testCase, cfg);

metrics = ...
    kwsim.analysis.summarizeSourceGeometry3D(cfg);

verifyEqual( ...
    testCase, ...
    metrics.source_count, ...
    8);

verifyGreaterThanOrEqual( ...
    testCase, ...
    metrics.in_plane_contact_count, ...
    2);

verifyGreaterThanOrEqual( ...
    testCase, ...
    metrics.out_of_plane_contact_count, ...
    6);

truth = ...
    kwsim.three_d.buildMaterialMaps(cfg);

verifyEqual( ...
    testCase, ...
    sort(unique(truth.material_id_xyz)), ...
    uint16([1; 2]));

end


