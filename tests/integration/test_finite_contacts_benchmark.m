%TEST_FINITE_CONTACTS_BENCHMARK
% Integration test for compact finite-contact field regimes.

tests = functiontests(localfunctions);

end

function setupOnce(~)

root = fileparts( ...
    fileparts(fileparts(mfilename('fullpath'))));

addpath(fullfile(root, 'src'));
addpath(fullfile(root, 'benchmarks'));

end

function testCompactFiniteContactSuitePasses(testCase)

configurations = struct();

configurations.directional = ...
    kwsim_benchmarks.finite_contacts_2d.compactConfig( ...
        "directional");

configurations.partially_diffuse = ...
    kwsim_benchmarks.finite_contacts_2d.compactConfig( ...
        "partially_diffuse");

configurations.diffuse = ...
    kwsim_benchmarks.finite_contacts_2d.compactConfig( ...
        "diffuse");

validation = ...
    kwsim_benchmarks.finite_contacts_2d.run( ...
        configurations);

verifyTrue( ...
    testCase, validation.valid, validation.summary);

verifyEqual( ...
    testCase, ...
    validation.metrics.solver_channel_count, ...
    [12, 24, 24]);

verifyEqual( ...
    testCase, ...
    validation.metrics.contact_span_m, ...
    4e-3 * ones(1, 3), ...
    'AbsTol', 1e-14);

verifyEqual( ...
    testCase, ...
    validation.benchmark, ...
    "finite_contacts_2d");

end
