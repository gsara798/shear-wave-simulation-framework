function report = assessValidationSample(sample, options)
%ASSESSVALIDATIONSAMPLE Check whether a 2D field can support REQ validation.
%
% The window length is defined as:
%
%   Lwin = WindowWavelengths * CsGuessMPerS / frequency_hz
%
% The number of required samples is conservative: N points must span at
% least Lwin, so N = ceil(Lwin / spacing) + 1.

arguments
    sample struct

    options.CsGuessMPerS (1,1) double = 3.0

    options.WindowWavelengths (1,1) double = 2.0

    options.MinimumPlacementsPerAxis (1,1) double = 5
end

validateInputs(sample, options);

wavefield = sample.wavefield_complex_zx;

Nz = size(wavefield, 1);
Nx = size(wavefield, 2);

dx_m = resolveSpacing( ...
    sample, ...
    "dx_m", ...
    sample.axes.x_m);

dz_m = resolveSpacing( ...
    sample, ...
    "dz_m", ...
    sample.axes.z_m);

frequency_hz = double(sample.frequency_hz);

wavelength_guess_m = ...
    options.CsGuessMPerS / frequency_hz;

window_length_m = ...
    options.WindowWavelengths * ...
    wavelength_guess_m;

window_points_x = ...
    ceil(window_length_m / dx_m) + 1;

window_points_z = ...
    ceil(window_length_m / dz_m) + 1;

placements_x = max( ...
    Nx - window_points_x + 1, ...
    0);

placements_z = max( ...
    Nz - window_points_z + 1, ...
    0);

field_finite = all( ...
    isfinite(wavefield), ...
    "all");

field_energy = double(sum( ...
    abs(wavefield).^2, ...
    "all"));

field_nonzero = ...
    isfinite(field_energy) && ...
    field_energy > 0;

checks = repmat( ...
    emptyCheck(), ...
    0, 1);

addCheck( ...
    "finite_wavefield", ...
    field_finite, ...
    double(field_finite), ...
    1, ...
    "The complex wavefield must contain only finite values.");

addCheck( ...
    "nonzero_wavefield_energy", ...
    field_nonzero, ...
    field_energy, ...
    0, ...
    "The complex wavefield must contain nonzero energy.");

addCheck( ...
    "x_window_fits", ...
    Nx >= window_points_x, ...
    Nx, ...
    window_points_x, ...
    "The REQ window must fit along x.");

addCheck( ...
    "z_window_fits", ...
    Nz >= window_points_z, ...
    Nz, ...
    window_points_z, ...
    "The REQ window must fit along z.");

addCheck( ...
    "minimum_x_placements", ...
    placements_x >= ...
        options.MinimumPlacementsPerAxis, ...
    placements_x, ...
    options.MinimumPlacementsPerAxis, ...
    "The field must support multiple REQ window positions along x.");

addCheck( ...
    "minimum_z_placements", ...
    placements_z >= ...
        options.MinimumPlacementsPerAxis, ...
    placements_z, ...
    options.MinimumPlacementsPerAxis, ...
    "The field must support multiple REQ window positions along z.");

report = struct();

report.valid = ...
    all([checks.pass]);

report.checks = ...
    checks;

report.parameters = struct();

report.parameters.cs_guess_m_s = ...
    options.CsGuessMPerS;

report.parameters.window_wavelengths = ...
    options.WindowWavelengths;

report.parameters.minimum_placements_per_axis = ...
    options.MinimumPlacementsPerAxis;

report.frequency_hz = ...
    frequency_hz;

report.wavelength_guess_m = ...
    wavelength_guess_m;

report.window_length_m = ...
    window_length_m;

report.window_points_x = ...
    window_points_x;

report.window_points_z = ...
    window_points_z;

report.sample_points_x = ...
    Nx;

report.sample_points_z = ...
    Nz;

report.sample_span_x_m = ...
    max(Nx - 1, 0) * dx_m;

report.sample_span_z_m = ...
    max(Nz - 1, 0) * dz_m;

report.placements_x = ...
    placements_x;

report.placements_z = ...
    placements_z;

report.field_energy = ...
    field_energy;

report.summary = sprintf([ ...
    'REQ-ready=%d, sample=%dx%d, span x/z=%.3f/%.3f mm, ', ...
    'window=%.3f mm (%d x %d points), ', ...
    'placements x/z=%d/%d'], ...
    report.valid, ...
    Nz, ...
    Nx, ...
    1e3 * report.sample_span_x_m, ...
    1e3 * report.sample_span_z_m, ...
    1e3 * window_length_m, ...
    window_points_x, ...
    window_points_z, ...
    placements_x, ...
    placements_z);

    function addCheck( ...
            name, pass, value, threshold, meaning)

        check = emptyCheck();

        check.name = string(name);
        check.pass = logical(pass);
        check.value = double(value);
        check.threshold = double(threshold);
        check.meaning = string(meaning);

        checks(end + 1, 1) = check;

    end

end


function validateInputs(sample, options)

required_fields = [
    "wavefield_complex_zx"
    "axes"
    "frequency_hz"
];

for field_name = required_fields.'
    if ~isfield(sample, field_name)
        error("kwsim:InvalidReqValidationSample", ...
            "REQ sample is missing field '%s'.", ...
            field_name);
    end
end

if options.CsGuessMPerS <= 0
    error("kwsim:InvalidReqReadinessParameters", ...
        "CsGuessMPerS must be positive.");
end

if options.WindowWavelengths <= 0
    error("kwsim:InvalidReqReadinessParameters", ...
        "WindowWavelengths must be positive.");
end

if options.MinimumPlacementsPerAxis < 1 || ...
        options.MinimumPlacementsPerAxis ~= ...
            fix(options.MinimumPlacementsPerAxis)

    error("kwsim:InvalidReqReadinessParameters", ...
        "MinimumPlacementsPerAxis must be a positive integer.");
end

end


function spacing_m = resolveSpacing( ...
    sample, spacing_name, axis_m)

spacing_m = NaN;

if isfield(sample, "spacing") && ...
        isfield(sample.spacing, spacing_name)

    spacing_m = double( ...
        sample.spacing.(spacing_name));
end

if ~isscalar(spacing_m) || ...
        ~isfinite(spacing_m) || ...
        spacing_m <= 0

    axis_m = double(axis_m(:));

    if numel(axis_m) < 2
        error("kwsim:InvalidReqValidationSample", ...
            "Cannot determine spatial sampling.");
    end

    spacing_m = median(diff(axis_m));
end

if ~isfinite(spacing_m) || ...
        spacing_m <= 0
    error("kwsim:InvalidReqValidationSample", ...
        "Spatial sampling must be positive.");
end

end


function check = emptyCheck()

check = struct( ...
    "name", "", ...
    "pass", false, ...
    "value", NaN, ...
    "threshold", NaN, ...
    "meaning", "");

end
